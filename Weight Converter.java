public class WeightConverter 
{
    public static void main (String[] args)
    {
        double a = 10;
        double b = 50;
        double c = 100;
        double conversion = 0.454;
        
        System.out.println("10 lbs. converts to " + a*conversion + "kg");
        System.out.println("50 lbs. converts to " + b*conversion + "kg"); 
        System.out.println("100 lbs. converts to " + c*conversion + "kg");
    }
    
}




